import numpy as np

# 1. 原始数据
a = np.array([0.0, 0.0, 0.07292, 0.34375, 0.47708, 0.54375, 0.61042, 0.65   , 0.68125,
       0.7    , 0.70833, 0.71875, 0.71875, 0.72708, 0.72917, 0.74167,
       0.7375 , 0.74375, 0.74375, 0.7375 , 0.73958, 0.73333, 0.75208,
       0.74375, 0.75417, 0.73542])

eff_efc = np.array([
 0.05054692, 0.15164075, 0.25273458, 0.35382841, 0.36914187, 0.37134407,
 0.36507066, 0.33920159, 0.29714153, 0.25445677, 0.22272863, 0.19704588,
 0.1759734, 0.16102403, 0.14879484, 0.13987374, 0.13156198, 0.12325022,
 0.11657374, 0.11192995, 0.10728616, 0.10264237, 0.09799858, 0.09335479,
 0.08871099, 0.06317014
])

eff_epc = np.array([
 0.01927915, 0.05783747, 0.09639579, 0.13495411, 0.19144159, 0.2499022,
 0.31009281, 0.37703798, 0.44074001, 0.49834341, 0.53985341, 0.5713157,
 0.59238818, 0.60934337, 0.62462708, 0.63681888, 0.64845304, 0.6600872,
 0.66854755, 0.67272479, 0.67690203, 0.68107927, 0.68525651, 0.68943375,
 0.693611,   0.71658582])

# 2. 对齐数组长度（因为 a 有 25 个元素，后两个有 26 个元素）
n = len(a)
efc_part = eff_efc[:n]
epc_part = eff_epc[:n]

# 3. 计算总和（分母）
total_ratio = efc_part + epc_part

# 4. 计算两个部分的比例
# 注意：第一个元素两者都为 0，直接除以 0 会产生警告。这里使用 np.where 处理，如果总和为 0 则比例为 0
ratio_efc = np.where(total_ratio != 0, efc_part / total_ratio, 0.0)
ratio_epc = np.where(total_ratio != 0, epc_part / total_ratio, 0.0)

# 如果第一个元素两边都为0，你可能希望平分，如果是的话可以解开下面两行的注释：
# ratio_efc[total_ratio == 0] = 0.5
# ratio_epc[total_ratio == 0] = 0.5

# 5. 将数组 a 乘上对应的比例，拆分成两个新数组
a_efc = a * ratio_efc
a_epc = a * ratio_epc

# 6. 打印结果验证
print("拆分后的第一个数组 (对应 eff_efc 比例):\n", a_efc)
print("\n拆分后的第二个数组 (对应 eff_epc 比例):\n", a_epc)

# 验证相加是否等于原数组 a
print("\n两数组相加是否还原为 a:", np.allclose(a_efc + a_epc, a))