# 2. 准备数据进行提取和插值
data_dict = {
    "eff_efc": eff_efc,
    "eff_epc": eff_epc,
    "eff_mufc": eff_mufc,
    "eff_mupc": eff_mupc,
    "crosssection_e": crosssection_e,
    "crosssection_ebar": crosssection_ebar,
    "crosssection_mu": crosssection_mu,
    "crosssection_mubar": crosssection_mubar
}

interpolated_results = {}

# 设置等比例插值网格
# 原始的 60 个点分布在 0 到 59
x_original = np.arange(60)                  

# 前 30 个元素原本对应的真实物理索引是 0 到 29 (步长为1)
x_30 = np.arange(30)                        

# 我们希望新的插值数据依然只分布在 [0, 29] 的区间内，但是插值恢复出 60 个高密度点
x_target = np.linspace(0, 29, 60)           

print("# ================== 插值后的 60 维数据 (只针对前30个数据点区间) ==================\n")

for name, original_arr in data_dict.items():
    # 提取前 30 个数据
    extracted_30 = original_arr[:30]
    
    # 使用三次样条插值 (make_interp_spline)
    # 输入 x 范围为 0~29 (30个点)，插值输出 x 范围依然为 0~29 (重采样为 60个点)
    spline = make_interp_spline(x_30, extracted_30, k=3)
    interpolated_60 = spline(x_target)
    
    # 强制将结果保存
    interpolated_results[name] = interpolated_60
    
    # 按照 np.array 格式原样打印出来，方便直接复制使用
    formatted_array = np.array2string(interpolated_60, separator=', ', formatter={'float_kind':lambda x: f"{x:.8f}"})
    print(f"    {name}_interpolated = np.array([\n        {formatted_array[1:-1]}\n    ])\n")

# 3. 绘图对比原始数据和插值后的数据
plt.figure(figsize=(15, 12))
plt.rcParams['font.sans-serif'] = ['SimHei', 'DejaVu Sans']  # 支持中文显示
plt.rcParams['axes.unicode_minus'] = False  # 正常显示负号

keys = list(data_dict.keys())
for i, name in enumerate(keys):
    plt.subplot(4, 2, i + 1)
    
    # 1. 绘制完整的原始 60 点数据 (覆盖 0 到 59)
    plt.plot(x_original, data_dict[name], 'k-', alpha=0.3, label='原始完整数据 (60点, 0~59)', linewidth=2.5)
    
    # 2. 绘制实际参与插值的 30 个源数据点 (仅落在 0 到 29 区间，步长为1)
    plt.scatter(x_30, data_dict[name][:30], color='red', s=20, zorder=3, label='用于插值的前30点 (0~29)')
    
    # 3. 绘制重插值出的 60 个高密度点数据 (等比例落在 0 到 29 区间)
    plt.plot(x_target, interpolated_results[name], 'b--', alpha=0.9, label='重插值高密数据 (60点, 0~29)', linewidth=1.5)
    
    # 设置相同的 X 轴范围，使所有子图保持一致的缩放比例
    plt.xlim(-2, 61)
    plt.title(name, fontsize=12, fontweight='bold')
    plt.grid(True, linestyle=':', alpha=0.6)
    plt.legend(fontsize=9)

plt.tight_layout()
